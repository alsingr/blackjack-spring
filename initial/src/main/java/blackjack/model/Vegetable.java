package blackjack.model;

/**
 *
 * Vegetable
 *
 */
public class Vegetable {
    private final long id;
    private final String name;


    public Vegetable(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }




}
